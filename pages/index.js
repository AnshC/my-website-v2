/** @jsxImportSource theme-ui */
import Head from 'next/head'
import Link from 'next/link'
import { FaLongArrowAltRight } from 'react-icons/fa'
import { annotate } from 'rough-notation'
import styles from '../styles/Home.module.css'
import { useEffect } from 'react'

export default function Home() {
  useEffect(()=>{
    const description = document.querySelector('#desc');
    const descAnnotation = annotate(description, { type: 'underline', color: "#fff", multiline: true });
    descAnnotation.show() 
    const icons = document.querySelectorAll('.icon')
    icons.forEach((icon)=>{
      const annotation = annotate(icon, { type: 'underline', color: "#92bcea" });
      icon.addEventListener('mouseover', ()=> {
        annotation.show()
      });
      icon.addEventListener('mouseout', ()=> {
        annotation.hide()
      })
    })
  }, [])
  const customStyles = {
    icon: {
      marginRight: '10px',
    }
  }
  return (
    <div className={styles.container}>

      <Head>
        <title>Ansh's Website</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <p sx={{ color: "text" }}>Welcome to my digital canvas. A collection of <span id="desc">design, code, and expression</span> in one website. While you're here, feel free to explore my:</p>
        <ul>
          <li className="icon"  sx={{ color: "grey", ':hover': { color: "accent" } }}>
            <Link href="/www">
              <a><FaLongArrowAltRight style={customStyles.icon}/><span> Web Development Projects</span></a>
            </Link>
          </li>
          <li className="icon"  sx={{ color: "grey", ':hover': { color: "accent" } }}>
            <Link href="/design">
              <a><FaLongArrowAltRight style={customStyles.icon}/><span> Graphic Design Portfolio</span></a>
            </Link>
          </li>
          <li className="icon"  sx={{ color: "grey", ':hover': { color: "accent" } }}>
            <Link href="/resume">
              <a><FaLongArrowAltRight style={customStyles.icon}/><span> Resume</span></a>
            </Link>
          </li>
        </ul>
      </main>  
    </div>
  )
}
